# Convertendo um valor booleano para uma string
new_string = str(True)
print(type(new_string))

# Convertendo um valor inteiro para uma string
new_string = str(123)
print(type(new_string))

# Convertendo um valor float para uma string
new_string = str(3.14)
print(type(new_string))

# Convertendo um valor None para uma string
new_string = str(None)
print(type(new_string))